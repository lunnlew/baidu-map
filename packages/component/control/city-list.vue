<template>
    <div>
        <slot></slot>
    </div>
</template>
<script setup lang="ts">
import { watch, computed, useAttrs, ref, onUnmounted } from 'vue'
import { map, state } from '../../lib/map'
import { addCityListControl } from '../../lib/control'
import { bindEvents, extractEmitEvents } from '../../utils/util'
const props = withDefaults(
    defineProps<{
        anchor?: number
        offset?: [number, number]
        show?: boolean
    }>(),
    {
        anchor: 0,
        offset: () => [50, 80],
        show: true,
    }
)
const attrs = useAttrs()
const emit = defineEmits({})
const isShow = computed(() => state.value.map_inited && props.show)
const options = computed(() => props)
const bm = ref()
watch(
    () => isShow.value,
    val => {
        if (val) {
            bm.value = bindEvents(addCityListControl(options.value), extractEmitEvents(attrs), emit)
        } else {
            bm.value && map.value?.removeControl(bm.value)
            bm.value = null
        }
    },
    {
        immediate: true,
    }
)
onUnmounted(() => {
    bm.value && map.value?.removeControl(bm.value)
    bm.value = null
})
defineExpose({
    bmobj: bm.value,
})
</script>
<script lang="ts">
export default {
    name: 'CityListControl',
}
</script>
